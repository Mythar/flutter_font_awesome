class FaIconData {
  String? name;
  int? codePoint;
  String? fontFamily;

  FaIconData({this.name, this.codePoint, this.fontFamily});

  factory FaIconData.fromName(String? name) {
    String _name = 'far fa-question-circle';
    int _codePoint = FontAwesomeIcons.icons['far fa-question-circle'];
    String _fontFamily = 'FontAwesomeRegular';

    if (name != null) {
      name = name.toLowerCase();
      if (FontAwesomeIcons.icons.containsKey(name)) {
        _name = name;
      }
    }

    if (_name.startsWith('fas')) {
      _codePoint = FontAwesomeIcons.icons[_name];
      _fontFamily = 'FontAwesomeSolid';
    } else if (_name.startsWith('far')) {
      _codePoint = FontAwesomeIcons.icons[_name];
      _fontFamily = 'FontAwesomeRegular';
    } else if (_name.startsWith('fal')) {
      _codePoint = FontAwesomeIcons.icons[_name];
      _fontFamily = 'FontAwesomeLight';
    } else if (_name.startsWith('fad')) {
      _codePoint = FontAwesomeIcons.icons[_name];
      _fontFamily = 'FontAwesomeDuotone';
    } else if (_name.startsWith('fab')) {
      _codePoint = FontAwesomeIcons.icons[_name];
      _fontFamily = 'FontAwesomeBrands';
    } else if (_name.startsWith('fat')) {
      _codePoint = FontAwesomeIcons.icons[_name];
      _fontFamily = 'FontAwesomeThin';
    }

    return FaIconData(
      name: _name,
      codePoint: _codePoint,
      fontFamily: _fontFamily,
    );
  }
}

enum FaSearchFilter {
  contains,
  startsWith
}

List<String> searchFontAwesomeIcons({String? text, FaSearchFilter searchFilter = FaSearchFilter.contains}) {
  List<String> l = [];

  if ((text != null) && (text != '')) {
    text = text.toLowerCase();
    for (String key in FontAwesomeIcons.icons.keys) {
      switch (searchFilter) {
        case FaSearchFilter.contains:
          if (key.contains(text)) {
            l.add(key);
          }
          break;
        case FaSearchFilter.startsWith:
          if (key.startsWith(text)) {
            l.add(key);
          }
          break;
      }
    }
  }

  return l;
}

class FaIcon extends StatelessWidget {
  final String? name;
  final double? size;
  final Color? color;
  final Color? secondaryColor;
  final double? opacity;
  final String? semanticLabel;
  final Widget? child;
  final bool? alignToParent;

  const FaIcon(
      this.name, {
        Key? key,
        this.size,
        this.color,
        this.secondaryColor,
        this.opacity,
        this.semanticLabel,
        this.child,
        this.alignToParent,
      }
      )  : super(key: key);

  Widget buildIcon(int codePoint, String fontFamily, double s, Color c) {
    return RichText(
      overflow: TextOverflow.visible,
      textDirection: TextDirection.ltr,
      text: TextSpan(
        text: String.fromCharCode(codePoint),
        style: TextStyle(
          inherit: false,
          color: c,
          fontSize: s,
          fontFamily: fontFamily,
        ),
      ),
    );
  }

  @override
  Widget build(BuildContext context) {
    // Opacity and color
    final double _opacity = opacity ?? 1.0;

    Color _primaryColor = color ?? Theme.of(context).primaryColor;
    Color _secondaryColor = secondaryColor ?? Theme.of(context).primaryColorLight;

    if (_opacity != 1.0) {
      _primaryColor = _primaryColor.withOpacity(_primaryColor.opacity * _opacity);
      _secondaryColor = _secondaryColor.withOpacity(_secondaryColor.opacity * _opacity);
    }

    // Align
    bool _alignToParent = false;
    if (alignToParent != null) {
      _alignToParent = alignToParent!;
    }

    // Size
    final _size = size ?? 25.0;

    // Icon data
    final FaIconData data = FaIconData.fromName(name);

    // Icon widget
    Widget iconWidget;

    // Check for duo tone / child
    if (child != null) {
      if (data.fontFamily == 'FontAwesomeDuotone') {
        iconWidget = Semantics(
          label: semanticLabel,
          child: ExcludeSemantics(
              child: Stack(
                children: <Widget>[
                  buildIcon(data.codePoint!, data.fontFamily!, _size, _primaryColor),
                  buildIcon(data.codePoint! + 0x100000, data.fontFamily!, _size, _secondaryColor),
                  child!,
                ],
              )
          ),
        );
      } else {
        iconWidget = Semantics(
          label: semanticLabel,
          child: ExcludeSemantics(
              child: Stack(
                children: <Widget>[
                  buildIcon(data.codePoint!, data.fontFamily!, _size, _primaryColor),
                  child!,
                ],
              )
          ),
        );
      }
    } else if (data.fontFamily == 'FontAwesomeDuotone') {
      iconWidget = Semantics(
        label: semanticLabel,
        child: ExcludeSemantics(
            child: Stack(
              children: <Widget>[
                buildIcon(data.codePoint!, data.fontFamily!, _size, _primaryColor),
                buildIcon(data.codePoint! + 0x100000, data.fontFamily!, _size, _secondaryColor),
              ],
            )
        ),
      );
    } else {
      iconWidget = Semantics(
        label: semanticLabel,
        child: ExcludeSemantics(
          child: buildIcon(data.codePoint!, data.fontFamily!, _size, _primaryColor),
        ),
      );
    }

    if (_alignToParent) {
      return Align(
        alignment: Alignment.center,
        child: iconWidget,
      );
    } else {
      return iconWidget;
    }
  }
}

